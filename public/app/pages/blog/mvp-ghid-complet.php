    <p class="blog-lead">Un MVP (Minimum Viable Product) este cea mai simplă versiune a produsului tău care rezolvă problema principală a utilizatorilor. Nu trebuie să fie perfect — trebuie să fie funcțional și lansat.</p>

    <h2>Ce este un MVP (și ce NU este)</h2>
    <p>MVP-ul nu este o versiune „slabă" sau „neterminată" a produsului. Este versiunea focalizată pe <strong>funcționalitățile esențiale</strong> care validează ideea de business. Totul în plus vine după ce ai confirmat că piața există.</p>

    <h2>Pasul 1: Definește problema core</h2>
    <p>Începe cu o singură propoziție: „Produsul meu rezolvă [problemă] pentru [audiență]." Dacă nu poți articula clar problema, nu ești pregătit să construiești.</p>

    <h3>Exemple bune:</h3>
    <ul>
      <li>„Ajutăm restaurantele locale să primească comenzi online fără comisioane mari."</li>
      <li>„Oferim freelancerilor un sistem simplu de facturare și tracking ore."</li>
      <li>„Permitem echipelor remote să colaboreze pe documente în timp real."</li>
    </ul>

    <h2>Pasul 2: Listează funcționalitățile MUST-HAVE</h2>
    <p>Scrie toate funcționalitățile pe care le-ai vrea. Apoi taie jumătate. Apoi taie din nou. Ce rămâne sunt funcționalitățile MVP. Regula de aur: dacă utilizatorul poate folosi produsul fără o funcționalitate, nu este must-have.</p>

    <h2>Pasul 3: Alege un tech stack rapid</h2>
    <p>Pentru MVP-uri, recomandăm stacks-uri care permit dezvoltare rapidă:</p>
    <ul>
      <li><strong>Frontend:</strong> Next.js sau React — componente reutilizabile, routing simplu</li>
      <li><strong>Backend:</strong> Node.js cu Express sau Python cu FastAPI</li>
      <li><strong>Baza de date:</strong> PostgreSQL (structurat) sau MongoDB (flexibil)</li>
      <li><strong>Deploy:</strong> Vercel, Railway sau AWS cu Docker</li>
    </ul>

    <h2>Pasul 4: Sprint de 2 săptămâni</h2>
    <p>Structura ideală pentru un sprint MVP:</p>
    <ul>
      <li><strong>Ziua 1–2:</strong> Setup proiect, design system, baza de date</li>
      <li><strong>Ziua 3–7:</strong> Dezvoltare funcționalități core</li>
      <li><strong>Ziua 8–10:</strong> Integrări, autentificare, polish basic</li>
      <li><strong>Ziua 11–12:</strong> Testare, bug fixes, feedback intern</li>
      <li><strong>Ziua 13–14:</strong> Deploy, monitorizare, lansare soft</li>
    </ul>

    <h2>Pasul 5: Lansează și măsoară</h2>
    <p>Nu aștepta perfecțiunea. Lansează, colectează feedback de la primii utilizatori, și iterează rapid. Datele reale bat întotdeauna presupunerile.</p>

